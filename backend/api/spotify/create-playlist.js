function getJsonBody(req) {
  if (req.body && typeof req.body === "object") return req.body;
  if (typeof req.body === "string") {
    try {
      return JSON.parse(req.body);
    } catch {
      return null;
    }
  }
  return null;
}

async function searchTrack(accessToken, title, artist) {
  const q = `track:${title} artist:${artist}`;
  const params = new URLSearchParams({
    type: "track",
    limit: "1",
    market: "from_token",
    q
  });

  const response = await fetch(
    `https://api.spotify.com/v1/search?${params.toString()}`,
    {
      headers: { Authorization: `Bearer ${accessToken}` }
    }
  );

  if (!response.ok) return null;
  const data = await response.json();
  const item = data?.tracks?.items?.[0];
  return item?.uri || null;
}

async function addTracks(accessToken, playlistId, uris) {
  return fetch(`https://api.spotify.com/v1/playlists/${playlistId}/tracks`, {
    method: "POST",
    headers: {
      "Authorization": `Bearer ${accessToken}`,
      "Content-Type": "application/json"
    },
    body: JSON.stringify({ uris })
  });
}

module.exports = async (req, res) => {
  if (req.method !== "POST") {
    res.status(405).json({ error: "Method not allowed" });
    return;
  }

  const body = getJsonBody(req);
  const accessToken = body?.access_token;
  const name = body?.name || "PlaylistMaker";
  const tracks = Array.isArray(body?.tracks) ? body.tracks : [];
  const isPublic = body?.isPublic !== undefined ? !!body.isPublic : false;

  if (!accessToken || typeof accessToken !== "string") {
    res.status(400).json({ error: "access_token is required" });
    return;
  }

  if (!tracks.length) {
    res.status(400).json({ error: "tracks is required" });
    return;
  }

  try {
    const createRes = await fetch("https://api.spotify.com/v1/me/playlists", {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${accessToken}`,
        "Content-Type": "application/json"
      },
      body: JSON.stringify({
        name,
        public: isPublic,
        description: "Generated by PlaylistMaker"
      })
    });

    if (!createRes.ok) {
      const errText = await createRes.text();
      res.status(500).json({ error: "Create playlist failed", details: errText });
      return;
    }

    const playlist = await createRes.json();

    const uris = [];
    const missing = [];

    for (const t of tracks) {
      if (!t || !t.title || !t.artist) {
        missing.push(t);
        continue;
      }
      const uri = await searchTrack(accessToken, t.title, t.artist);
      if (uri) uris.push(uri);
      else missing.push(t);
    }

    let addedCount = 0;
    let skippedCount = 0;
    let addFailureDetails = "";

    if (uris.length > 0) {
      const addRes = await addTracks(accessToken, playlist.id, uris);
      if (addRes.ok) {
        addedCount = uris.length;
      } else {
        addFailureDetails = await addRes.text();

        // Some accounts/regions reject specific tracks with 403.
        // Fallback to per-track adds so we can keep partial success.
        if (addRes.status === 403) {
          for (const uri of uris) {
            const singleRes = await addTracks(accessToken, playlist.id, [uri]);
            if (singleRes.ok) {
              addedCount += 1;
            } else {
              skippedCount += 1;
            }
          }

          if (addedCount === 0) {
            skippedCount = uris.length;
          }
        } else {
          res.status(500).json({
            error: "Add tracks failed",
            details: addFailureDetails
          });
          return;
        }
      }
    }

    res.status(200).json({
      playlist_id: playlist.id,
      playlist_url: playlist.external_urls?.spotify,
      added_count: addedCount,
      missing_count: missing.length,
      skipped_count: skippedCount,
      note: skippedCount > 0
        ? "Playlist was created, but Spotify blocked some track additions (region/account restrictions)."
        : undefined,
      add_error: addFailureDetails || undefined
    });
  } catch (err) {
    res.status(500).json({ error: "Unexpected error", details: String(err) });
  }
};
